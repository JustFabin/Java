package trab.aparecido.javaproject;

public class DListNode {
	
	public Integer item;
	public DListNode next;
	public DListNode prev;
	
	public DListNode() {
	this.item = 0;
	this.next = null;
	this.prev = null;

	}
	
	public DListNode(Integer item) {
	this.item = item;
	this.next = null;
	this.prev = null;

	}
}
